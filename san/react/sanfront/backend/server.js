// 🔹 Route for calling **READ** functions (no gas fees)
app.get("/read/:functionName", async (req, res) => {
    try {
        const { functionName } = req.params;
        const args = req.query.args ? JSON.parse(req.query.args) : [];

        const result = await contract[functionName](...args);

        // ✅ Fix BigInt Serialization Issue
        const formattedResult = Array.isArray(result)
            ? result.map((item) => (typeof item === "bigint" ? item.toString() : item))
            : typeof result === "bigint"
            ? result.toString()
            : result;

        res.json({ success: true, data: formattedResult });
    } catch (error) {
        console.error(`❌ Error in read ${req.params.functionName}:`, error);
        res.status(500).json({ success: false, error: error.message });
    }
});

// 🔹 Route for calling **WRITE** functions (needs gas fees)
app.post("/write", async (req, res) => {
    try {
        const { functionName, args } = req.body;

        // ✅ Ensure Args are Passed
        if (!functionName || !Array.isArray(args)) {
            return res.status(400).json({ success: false, error: "Invalid request format." });
        }

        const tx = await contract[functionName](...args);
        await tx.wait();

        res.json({ success: true, txHash: tx.hash });
    } catch (error) {
        console.error(`❌ Error in write ${req.body.functionName}:`, error);
        res.status(500).json({ success: false, error: error.message });
    }
});

